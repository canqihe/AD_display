public class ImagesBean {

    private String groupId;
    private String linkUrl;
    private int plotDetailId;
    private int plotId;
    private int resourceInterval;
    private int tag;
    private int type;

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getLinkUrl() {
        return linkUrl;
    }

    public void setLinkUrl(String linkUrl) {
        this.linkUrl = linkUrl;
    }

    public int getPlotDetailId() {
        return plotDetailId;
    }

    public void setPlotDetailId(int plotDetailId) {
        this.plotDetailId = plotDetailId;
    }

    public int getPlotId() {
        return plotId;
    }

    public void setPlotId(int plotId) {
        this.plotId = plotId;
    }

    public int getResourceInterval() {
        return resourceInterval;
    }

    public void setResourceInterval(int resourceInterval) {
        this.resourceInterval = resourceInterval;
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
